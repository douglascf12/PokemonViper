import UIKit

extension UIColor {
    static var primary: UIColor {
        return UIColor(red: 1/255, green: 210/255, blue: 119/255, alpha: 1)
    }
    
    static var secundary: UIColor {
        return .black
    }
    
    static var activityBackground: UIColor {
        return .white
    }
}
